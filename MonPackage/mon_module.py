def dire_bonjour(firstname, lastname):
    """
        Affiche "Bonjour {firstname} {lastname}"
        :param firstname: Someone's firstname
        :param lastname: Someone's lastname
    """
    print(f"Bonjour {firstname} {lastname}")
    
def say_hello(firstname, lastname):
    """
        Affiche "Hello {firstname} {lastname}"
        :param firstname: Someone's firstname
        :param lastname: Someone's lastname
    """
    print(f"Hello {firstname} {lastname}")